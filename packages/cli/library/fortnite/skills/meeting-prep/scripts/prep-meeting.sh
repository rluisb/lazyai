#!/usr/bin/env bash
# prep-meeting.sh — Generate preparation materials for a specific meeting
# Usage: ./prep-meeting.sh <meeting-name>
# Example: ./prep-meeting.sh "Growth Team Standup"

set -euo pipefail

MEETING="${1:-}"
if [[ -z "$MEETING" ]]; then
    echo "Usage: $0 <meeting-name>"
    echo ""
    echo "Available meetings:"
    echo "  Growth Team Standup"
    echo "  Growth Team Prioritization"
    echo "  Services Guild Meeting"
    echo "  FrontEnd Guild Meeting"
    echo "  Teachable + Stripe Sync"
    echo "  Oncall Handoff"
    echo "  Growth Eng Weekly"
    echo "  Project Bee-Gone Sync"
    echo "  All Hands"
    echo "  Learnable"
    exit 1
fi

TODAY=$(date +"%Y-%m-%d")
DAY_OF_WEEK=$(date +"%u")  # 1=Monday, 7=Sunday
OUTPUT_DIR="docs/meeting-prep"
mkdir -p "$OUTPUT_DIR"
OUTPUT_FILE="$OUTPUT_DIR/${TODAY}-$(echo "$MEETING" | tr ' ' '-' | tr '[:upper:]' '[:lower:]').md"

echo "## Meeting: $MEETING" > "$OUTPUT_FILE"
echo "**Date:** $(date +"%B %d, %Y")" >> "$OUTPUT_FILE"
echo "**Prepared:** $(date +"%H:%M %Z")" >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"

case "$MEETING" in
    *"Standup"*)
        echo "### Yesterday" >> "$OUTPUT_FILE"
        echo "_Check Jira for issues updated yesterday, recent commits, PR activity_" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        echo "### Today" >> "$OUTPUT_FILE"
        echo "_Check Jira for issues in progress, planned work_" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        echo "### Blockers" >> "$OUTPUT_FILE"
        echo "_Check for blocked issues, pending reviews, dependencies_" >> "$OUTPUT_FILE"
        ;;
    *"Prioritization"*|*"Replenishment"*)
        echo "### Current Sprint Status" >> "$OUTPUT_FILE"
        echo "_Pull sprint velocity, completion rate_" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        echo "### Backlog Items" >> "$OUTPUT_FILE"
        echo "_List untriaged items, high-priority backlog_" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        echo "### Capacity vs Demand" >> "$OUTPUT_FILE"
        echo "_Compare team capacity to backlog size_" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        echo "### Decisions Needed" >> "$OUTPUT_FILE"
        echo "_List items requiring priority decisions_" >> "$OUTPUT_FILE"
        ;;
    *"Oncall"*|*"Operability"*)
        echo "### Recent Incidents" >> "$OUTPUT_FILE"
        echo "_Check for P1-P4 incidents in last week_" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        echo "### SLO Status" >> "$OUTPUT_FILE"
        echo "_Check SLO compliance, error budget_" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        echo "### On-Call Notes" >> "$OUTPUT_FILE"
        echo "_Review on-call handoff notes_" >> "$OUTPUT_FILE"
        ;;
    *"Sync"*|*"Weekly"*)
        echo "### Progress This Week" >> "$OUTPUT_FILE"
        echo "_Summarize completed work, in-progress items_" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        echo "### Milestones" >> "$OUTPUT_FILE"
        echo "_Check milestone progress, upcoming deadlines_" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        echo "### Blockers / Risks" >> "$OUTPUT_FILE"
        echo "_List any blockers or risks to discuss_" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        echo "### Next Week Goals" >> "$OUTPUT_FILE"
        echo "_Planned work for next week_" >> "$OUTPUT_FILE"
        ;;
    *"Guild"*|*"All Hands"*|*"Learnable"*)
        echo "### Updates" >> "$OUTPUT_FILE"
        echo "_Prepare relevant updates for this meeting_" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        echo "### Discussion Points" >> "$OUTPUT_FILE"
        echo "_List topics to raise_" >> "$OUTPUT_FILE"
        ;;
    *)
        echo "### Notes" >> "$OUTPUT_FILE"
        echo "_Meeting-specific preparation_" >> "$OUTPUT_FILE"
        ;;
esac

echo "" >> "$OUTPUT_FILE"
echo "---" >> "$OUTPUT_FILE"
echo "*Generated by prep-meeting.sh*" >> "$OUTPUT_FILE"

echo "✅ Meeting prep saved to: $OUTPUT_FILE"
echo ""
echo "Next steps:"
echo "1. Review the brief and fill in details"
echo "2. Check Jira/Confluence for latest status"
echo "3. Check PR status for pending reviews"
