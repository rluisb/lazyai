#!/usr/bin/env bash
# weekly-brief.sh — Generate consolidated weekly status brief
# Usage: ./weekly-brief.sh
# Run on Monday morning for full week overview

set -euo pipefail

TODAY=$(date +"%Y-%m-%d")
WEEK_START=$(date -v-monday +"%Y-%m-%d" 2>/dev/null || date -d "last monday" +"%Y-%m-%d" 2>/dev/null || echo "$TODAY")
OUTPUT_DIR="docs/meeting-prep"
mkdir -p "$OUTPUT_DIR"
OUTPUT_FILE="$OUTPUT_DIR/${TODAY}-weekly-brief.md"

echo "## Weekly Brief — Week of $WEEK_START" > "$OUTPUT_FILE"
echo "**Generated:** $(date +"%B %d, %Y at %H:%M %Z")" >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"

echo "### This Week's Meetings" >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"
echo "| Day | Meeting | Time (ET) | Prep Status |" >> "$OUTPUT_FILE"
echo "|-----|---------|-----------|-------------|" >> "$OUTPUT_FILE"
echo "| Mon | Growth Team Standup | 10:30-10:50 | ⬜ Pending |" >> "$OUTPUT_FILE"
echo "| Mon | Growth Team Prioritization | 10:30-11:30 | ⬜ Pending |" >> "$OUTPUT_FILE"
echo "| Mon | Services Guild Meeting | 13:00-14:00 | ⬜ Pending |" >> "$OUTPUT_FILE"
echo "| Mon | FrontEnd Guild Meeting | 16:00-17:00 | ⬜ Pending |" >> "$OUTPUT_FILE"
echo "| Tue | Teachable + Stripe Sync | 09:30-10:30 MT | ⬜ Pending |" >> "$OUTPUT_FILE"
echo "| Tue | Oncall Handoff & Operability | 11:00-11:30 | ⬜ Pending |" >> "$OUTPUT_FILE"
echo "| Tue | Growth Eng Weekly | 16:00-17:00 | ⬜ Pending |" >> "$OUTPUT_FILE"
echo "| Thu | Growth Team Standup | 10:30-10:50 | ⬜ Pending |" >> "$OUTPUT_FILE"
echo "| Thu | Project Bee-Gone Sync | 14:30-15:00 | ⬜ Pending |" >> "$OUTPUT_FILE"
echo "| Fri | Learnable | 14:00-14:30 | ⬜ Pending |" >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"

echo "### Active Work" >> "$OUTPUT_FILE"
echo "_Check Jira for issues in progress_" >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"

echo "### Completed Last Week" >> "$OUTPUT_FILE"
echo "_Check Jira for issues closed last week, recent merges_" >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"

echo "### Pending Reviews" >> "$OUTPUT_FILE"
echo "_Check PRs awaiting review, review requests from team_" >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"

echo "### Blockers" >> "$OUTPUT_FILE"
echo "_List any active blockers_" >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"

echo "### Focus Areas This Week" >> "$OUTPUT_FILE"
echo "_Define top 3 priorities for the week_" >> "$OUTPUT_FILE"
echo "1. " >> "$OUTPUT_FILE"
echo "2. " >> "$OUTPUT_FILE"
echo "3. " >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"

echo "---" >> "$OUTPUT_FILE"
echo "*Generated by weekly-brief.sh*" >> "$OUTPUT_FILE"

echo "✅ Weekly brief saved to: $OUTPUT_FILE"
echo ""
echo "Next steps:"
echo "1. Fill in active work from Jira"
echo "2. Check PR status for pending reviews"
echo "3. Define focus areas for the week"
echo "4. Run prep-meeting.sh before each meeting"
